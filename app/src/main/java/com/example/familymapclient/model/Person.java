package com.example.familymapclient.model;

public class Person {

    String personID;

    String firstName;

    String lastName;

    String gender;

    String fatherID;

    String motherID;

    String spouseID;

    public String getPersonID() {
        return personID;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getGender() {
        return gender;
    }

    public String getFatherID() {
        return fatherID;
    }

    public String getMotherID() {
        return motherID;
    }

    public String getSpouseID() {
        return spouseID;
    }
}
